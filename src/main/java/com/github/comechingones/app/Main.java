package com.github.comechingones.app;                  // ubica la clase en el paquete

public class Main {                                     // clase principal
    public static void main(String[] args) {            // punto de entrada del programa
        System.out.println("Sistema de Turnos Médicos - Comechingones v0.1"); // salida por consola
    }
}
// Línea por línea (rápido):
//        package ...; → organiza la clase dentro del paquete.
//        public class Main { → definís una clase pública llamada Main.
//        public static void main(String[] args) → método que Java ejecuta primero.
//        System.out.println(...) → imprime texto en la consola.